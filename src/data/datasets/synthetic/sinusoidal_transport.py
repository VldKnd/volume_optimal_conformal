# src/data/datasets/synthetic/sinusoidal_transport.py

import math

import torch

from configs.datasets.synthetic.sinusoidal_transport import (
    SinusoidalTransportDatasetConfig,
)
from data.datasets.base import DatasetSplits, XYData
from data.datasets.synthetic.base import BaseSyntheticDataset


class SinusoidalTransportDataset(BaseSyntheticDataset):
    """
    Conditional 2D target generated by a triangular sinusoidal transport.

        U ~ N(0, I_2)
        X ~ Uniform([x_low, x_high]^x_dim)

        Y_1 = U_1
        Y_2 = s(X) * U_2 + a(X) * sin(frequency * U_1 + phase)

        a(X) = amplitude + amplitude_x_scale * centered_x
        s(X) = vertical_scale * exp(vertical_scale_x_scale * centered_x)

    The Jacobian is triangular:

        [[1, 0],
         [a(X) * frequency * cos(frequency * u1 + phase), s(X)]]

    Therefore det DY/DU = s(X). For fixed x, Gaussian level sets become
    sinusoidal, wavy contours in target space. With vertical_scale_x_scale=0
    and vertical_scale=1, the map is volume-preserving for every x.
    """

    def __init__(self, config: SinusoidalTransportDatasetConfig):
        self.config = config
        self._splits: DatasetSplits | None = None

        self.device = torch.device(config.device)
        self.dtype = getattr(torch, config.dtype)

        if config.y_dim != 2:
            raise ValueError("SinusoidalTransportDataset requires y_dim = 2.")

        if config.x_high <= config.x_low:
            raise ValueError("x_high must be larger than x_low.")

        self._generator = torch.Generator(device="cpu")
        self._generator.manual_seed(config.seed)

    @property
    def x_dim(self) -> int:
        return self.config.x_dim

    @property
    def y_dim(self) -> int:
        return self.config.y_dim

    @property
    def n_total(self) -> int:
        return (self.config.n_train + self.config.n_calibration + self.config.n_test)

    @property
    def supports_density(self) -> bool:
        return True

    def sample_x(self, n: int) -> torch.Tensor:
        x = (
            self.config.x_low + (self.config.x_high - self.config.x_low) * torch.rand(
                n,
                self.config.x_dim,
                generator=self._generator,
                dtype=self.dtype,
            )
        )
        return x.to(self.device)

    def sample_source(self, n: int) -> torch.Tensor:
        u = torch.randn(
            n,
            self.y_dim,
            generator=self._generator,
            dtype=self.dtype,
        )
        return u.to(self.device)

    def sample_target(self, n: int) -> torch.Tensor:
        x = self.sample_x(n)
        return self.sample_conditional(x, n_samples=1).squeeze(1)

    def sample_conditional(
        self,
        x: torch.Tensor,
        n_samples: int = 1,
    ) -> torch.Tensor:
        """
        Sample Y | X=x.

        Args:
            x: (batch, x_dim)
            n_samples: number of samples per x

        Returns:
            y: (batch, n_samples, 2)
        """
        x = x.to(device=self.device, dtype=self.dtype)
        self._validate_x(x)

        u = torch.randn(
            x.shape[0],
            n_samples,
            self.y_dim,
            generator=self._generator,
            dtype=self.dtype,
        ).to(self.device)

        x_expanded = x[:, None, :].expand(
            x.shape[0],
            n_samples,
            self.x_dim,
        )

        return self.push_u_given_x(u=u, x=x_expanded)

    def push_u_given_x(
        self,
        u: torch.Tensor,
        x: torch.Tensor,
    ) -> torch.Tensor:
        """
        Push latent U forward to Y. The map is triangular for each x.

        Args:
            u: (..., 2)
            x: (..., x_dim)

        Returns:
            y: (..., 2)
        """
        u = u.to(device=self.device, dtype=self.dtype)
        x = x.to(device=self.device, dtype=self.dtype)
        self._validate_matching_shapes(point=u, x=x, point_name="u")

        u1 = u[..., 0:1]
        u2 = u[..., 1:2]

        amplitude, vertical_scale, _ = self._transport_parameters(x)
        wave = amplitude * torch.sin(self.config.frequency * u1 + self.config.phase)
        y1 = u1
        y2 = vertical_scale * u2 + wave

        return torch.cat([y1, y2], dim=-1)

    def push_y_given_x(
        self,
        y: torch.Tensor,
        x: torch.Tensor,
    ) -> torch.Tensor:
        """
        Pull Y back to latent U using the exact triangular inverse.

        Args:
            y: (..., 2)
            x: (..., x_dim)

        Returns:
            u: (..., 2)
        """
        y = y.to(device=self.device, dtype=self.dtype)
        x = x.to(device=self.device, dtype=self.dtype)
        self._validate_matching_shapes(point=y, x=x, point_name="y")

        y1 = y[..., 0:1]
        y2 = y[..., 1:2]

        amplitude, vertical_scale, _ = self._transport_parameters(x)
        wave = amplitude * torch.sin(self.config.frequency * y1 + self.config.phase)
        u1 = y1
        u2 = (y2 - wave) / vertical_scale

        return torch.cat([u1, u2], dim=-1)

    def log_det(
        self,
        x: torch.Tensor,
        u: torch.Tensor,
    ) -> torch.Tensor:
        """
        Return log |det D_u T_x(u)|.
        """
        x = x.to(device=self.device, dtype=self.dtype)
        u = u.to(device=self.device, dtype=self.dtype)
        self._validate_matching_shapes(point=u, x=x, point_name="u")

        _, _, log_vertical_scale = self._transport_parameters(x)
        return log_vertical_scale.squeeze(-1)

    def log_prob(
        self,
        x: torch.Tensor,
        y: torch.Tensor,
    ) -> torch.Tensor:
        """
        Compute log p(y | x) by exact change of variables.
        """
        u = self.push_y_given_x(y=y, x=x)

        log_base = -0.5 * (
            u.square().sum(dim=-1) + self.y_dim *
            torch.log(torch.tensor(
                2.0 * torch.pi,
                device=u.device,
                dtype=u.dtype,
            ))
        )
        _, _, log_abs_det = self._transport_parameters(
            x.to(device=u.device, dtype=u.dtype)
        )

        return log_base - log_abs_det.squeeze(-1)

    def prepare(self) -> None:
        x, y = self.sample_joint(self.n_total)

        n_train = self.config.n_train
        n_cal = self.config.n_calibration
        n_test = self.config.n_test

        self._splits = DatasetSplits(
            train=XYData(
                x=x[:n_train],
                y=y[:n_train],
            ),
            calibration=XYData(
                x=x[n_train:n_train + n_cal],
                y=y[n_train:n_train + n_cal],
            ),
            test=XYData(
                x=x[n_train + n_cal:n_train + n_cal + n_test],
                y=y[n_train + n_cal:n_train + n_cal + n_test],
            ),
        )

    def get_splits(self) -> DatasetSplits:
        if self._splits is None:
            self.prepare()

        assert self._splits is not None
        return self._splits

    def _validate_x(self, x: torch.Tensor) -> None:
        if x.shape[-1] != self.x_dim:
            raise ValueError(f"Expected x.shape[-1] = {self.x_dim}, got {x.shape[-1]}.")

    def _centered_covariate(self, x: torch.Tensor) -> torch.Tensor:
        midpoint = 0.5 * (self.config.x_low + self.config.x_high)
        return x[..., 0:1] - midpoint

    def _transport_parameters(
        self,
        x: torch.Tensor,
    ) -> tuple[torch.Tensor, torch.Tensor, torch.Tensor]:
        self._validate_x(x)

        centered_x = self._centered_covariate(x)
        amplitude = self.config.amplitude + self.config.amplitude_x_scale * centered_x
        log_vertical_scale = (
            math.log(self.config.vertical_scale) +
            self.config.vertical_scale_x_scale * centered_x
        )
        vertical_scale = torch.exp(log_vertical_scale)

        return amplitude, vertical_scale, log_vertical_scale

    def _validate_matching_shapes(
        self,
        point: torch.Tensor,
        x: torch.Tensor,
        point_name: str,
    ) -> None:
        if point.shape[:-1] != x.shape[:-1]:
            raise ValueError(
                f"Expected {point_name}.shape[:-1] == x.shape[:-1], got "
                f"{point.shape[:-1]} and {x.shape[:-1]}."
            )

        if point.shape[-1] != self.y_dim:
            raise ValueError(
                f"Expected {point_name}.shape[-1] = {self.y_dim}, "
                f"got {point.shape[-1]}."
            )

        self._validate_x(x)
